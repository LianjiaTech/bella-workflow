package com.ke.bella.workflow.enums;

public enum ResultCodeEnum {

    SUCCESS(200, "操作成功");

    private final int code;
    private final String message;

    ResultCodeEnum(int code, String message) {
        this.code = code;
        this.message = message;
    }

    public int getCode() {
        return this.code;
    }

    public String getMessage() {
        return this.message;
    }
}
